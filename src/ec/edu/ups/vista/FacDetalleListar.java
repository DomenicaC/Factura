    public void llenarDatos() {
        DefaultTableModel modelo = (DefaultTableModel) tblFacD.getModel();
        
        Set<FacturaDetalle> lista = contFacD.getLista();
        for (FacturaDetalle facD : lista) {
            Object[] datos = {facD.getCodigo(),
                facD.getCantidad(),
                facD.getTotal()};
            modelo.addRow(datos);
        }
    }


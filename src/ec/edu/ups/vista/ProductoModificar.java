    private void btnBuscarMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarMActionPerformed
        int codigo = Integer.parseInt(txtCodigoM.getText());
        Producto buscarP = contProd.read(codigo);
        System.out.println(buscarP);
        if (buscarP == null) {
            JOptionPane.showMessageDialog(null, "El codigo no existe");
        } else {
            txtNombreM.setText(buscarP.getNombre());
            txtPrecioM.setText(String.valueOf(buscarP.getPrecio()));
            contProd.delete(codigo);
            
        }
    }//GEN-LAST:event_btnBuscarMActionPerformed

    private void btnModificarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarPActionPerformed
        Producto prodB = new Producto();

        prodB.setNombre(txtNombreM.getText());
        prodB.setPrecio(Double.parseDouble(txtPrecioM.getText()));

        //prodB.setCodigo(Integer.parseInt(txtCodigoM.getText()));
        contProd.update(prodB);
        JOptionPane.showMessageDialog(null, "El producto fue actualizado con exito");
        System.out.println(prodB);
        txtNombreM.setText("");
        txtPrecioM.setText("");
    }//GEN-LAST:event_btnModificarPActionPerformed



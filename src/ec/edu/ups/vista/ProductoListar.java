    public void llenarDatosP(){
        DefaultTableModel modeloP = (DefaultTableModel) tblProducto.getModel();
        Set<Producto> lista = contProd.getLista();
        
        for (Producto prod : lista) {
            Object[] datosP = {prod.getCodigo(),
                prod.getNombre(),
                prod.getPrecio()};
            modeloP.addRow(datosP);
        }
    }
    
    
